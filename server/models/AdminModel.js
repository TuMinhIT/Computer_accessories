import mongoose from "mongoose";

const AdminSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
});

const AdminModel =
  mongoose.models.admin || mongoose.model("admin", AdminSchema);
